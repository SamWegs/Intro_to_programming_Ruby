# Examples to type while reading

# EX. 1: with double quotes
"The man said, 'Hi there!'"

# Ex. 2: with single quotes and escaping
'The man said, \'Hi there!\''

# interpolation - the value 'a' is pulled directly into line 11.
a = "ten"
puts "My favorite number is #{a}!"

# Symbols are made puting a colon before a word.
:name
:a_symbol
:"This is also a symbol"
